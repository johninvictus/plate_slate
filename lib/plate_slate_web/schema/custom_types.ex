defmodule PlateSlateWeb.Schema.CustomTypes do
  use Absinthe.Schema.Notation

  scalar :date do
    parse(fn input ->
      with %Absinthe.Blueprint.Input.String{value: value} <- input,
           {:ok, date} <- Date.from_iso8601(value) do
        {:ok, date}
      else
        _ ->
          :error
      end
    end)

    serialize(fn date ->
      Date.to_iso8601(date)
    end)
  end
end
